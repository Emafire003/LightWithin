//This will change the type and the target if needed of one's light. Randomly.
public class LuxmutuaTestBerryItem extends Item {

    public LuxmutuaTestBerryItem(Settings settings) {
        super(settings);
    }

    @Override
    public TypedActionResult<ItemStack> use(World world, PlayerEntity user, Hand hand) {
        LightComponent component = LightWithin.LIGHT_COMPONENT.get(user);
        component.setTargets(TargetType.ENEMIES);
        component.setType(InnerLightType.FROST);

        if (this.isFood()) {
            ItemStack itemStack = user.getStackInHand(hand);
            if (user.canConsume(this.getFoodComponent().isAlwaysEdible())) {
                user.setCurrentHand(hand);
                return TypedActionResult.consume(itemStack);
            } else {
                return TypedActionResult.fail(itemStack);
            }
        } else {
            return TypedActionResult.pass(user.getStackInHand(hand));
        }
    }

    @Override
    public void appendTooltip(ItemStack stack, @Nullable World world, List<Text> tooltip, TooltipContext context) {
        tooltip.add(Text.literal("Test berry!"));
    }
}
